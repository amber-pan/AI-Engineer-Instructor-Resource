# Taking two numbers as input from the user
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

# Applying comparison operators and displaying results
print(f"{a} > {b}  :", a > b)
print(f"{a} < {b}  :", a < b)
print(f"{a} >= {b} :", a >= b)
print(f"{a} <= {b} :", a <= b)
print(f"{a} == {b} :", a == b)
print(f"{a} != {b} :", a != b)
